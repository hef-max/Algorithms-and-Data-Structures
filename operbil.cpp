#include<stdio.h>

int main(){
	
	int a; 
	int b;
	
	
	//penjumlahan//
	
    scanf("%d", &a);
    scanf("%d", &b);
    
    printf("%d\n", a + b);
    
    
    //pengurangan//
    
    scanf("%d", &a);
    scanf("%d", &b);
    
    printf("%d\n", a - b);
    
    
	//perkalian//
	
    scanf("%d", &a);
    scanf("%d", &b);
    
    printf("%d\n", a * b); 
	
	
	//pembagian//
	
    scanf("%d", &a);
    scanf("%d", &b);
    
    printf("%d\n", a / b);
	
	
	//modulus//
	
    scanf("%d", &a);
    scanf("%d", &b);
    
    printf("%d\n", a % b); 
    
    
	return 0;
}
